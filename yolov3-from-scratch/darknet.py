class Darknet(nn.Module):
    def __init__(self):
        super(Darknet, self).__init__()
        with open("blocks.pkl", "rb") as f:
            self.blocks = pickle.load(f)
        with open("net_info.pkl", "rb") as f:
            self.net_info = pickle.load(f)
        with open("module_list.pkl", "rb") as f:
            self.module_list = pickle.load(f)

    def forward(self, x):
        modules = self.blocks[1:]
        outputs = {}
        write = 0

        for i, module in enumerate(modules):        
            module_type = (module["type"])
            
            if module_type == "convolutional" or module_type == "upsample":
                x = self.module_list[i](x)
    
            elif module_type == "route":
                layers = module["layers"]
                layers = layers.split(',')
                layers = [int(a) for a in layers]
    
                if (layers[0]) > 0:
                    layers[0] = layers[0] - i
    
                if len(layers) == 1:
                    x = outputs[i + (layers[0])]
    
                else:
                    if (layers[1]) > 0:
                        layers[1] = layers[1] - i
    
                    map1 = outputs[i + layers[0]]
                    map2 = outputs[i + layers[1]]
                    x = torch.cat((map1, map2), 1)
            elif  module_type == "shortcut":
                from_ = int(module["from"])
                x = outputs[i-1] + outputs[i+from_]
    
            elif module_type == 'yolo':        
                anchors = self.module_list[i][0].anchors
                inp_dim = int (self.net_info["height"])
                num_classes = int (module["classes"])
                x = predict_transform(x, inp_dim, anchors, num_classes)
                if not write:
                    detections = x
                    write = 1
        
                else:       
                    detections = torch.cat((detections, x), 1)
            outputs[i] = x
            
        #set_trace()
        return detections

    def get_all_predict(self, img):
        model_inp = prep_image(img)
        im_dim = img.shape[1], img.shape[0]
        im_dim = torch.FloatTensor(im_dim).repeat(1,2)
        im_dim = im_dim.cuda()

        model_inp = model_inp.cuda()
        with torch.no_grad():
            output = self(model_inp)

        output = write_results(output, confidence, num_classes, nms_conf = nms_thresh)
        if type(output) == int:
            return output

        im_dim = im_dim.repeat(output.size(0), 1)
        scaling_factor = torch.min(416/im_dim,1)[0].view(-1,1)

        output[:,[1,3]] -= (inp_dim - scaling_factor*im_dim[:,0].view(-1,1))/2
        output[:,[2,4]] -= (inp_dim - scaling_factor*im_dim[:,1].view(-1,1))/2

        output[:,1:5] /= scaling_factor

        for i in range(output.shape[0]):
            output[i, [1,3]] = torch.clamp(output[i, [1,3]], 0.0, im_dim[i,0])
            output[i, [2,4]] = torch.clamp(output[i, [2,4]], 0.0, im_dim[i,1])
        return output

